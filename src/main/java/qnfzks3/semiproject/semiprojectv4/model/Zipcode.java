package qnfzks3.semiproject.semiprojectv4.model;



/*우편번호 데이터를 담는 클라스*/

public class Zipcode {
    private String zipcode;
    private String sido;
    private String gugun;

    private String dong;

    private String ri;
    private String bunji;

    public Zipcode() {
    }

    public Zipcode(String zipcode, String sido, String gugun, String dong, String ri, String bunji) {
        this.zipcode = zipcode;
        this.sido = sido;
        this.gugun = gugun;
        this.dong = dong;
        this.ri = ri;
        this.bunji = bunji;
    }





}
